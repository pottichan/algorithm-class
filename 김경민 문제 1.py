# 계단 오르는 방법 수 계산 (Bottom-up 동적계획법)

# 1. 사용자 입력
n = int(input("계단의 개수를 입력하시오: "))

# 2. DP 테이블 생성
dp = [0] * (n + 1)

# 3. 초기값 설정
dp[0] = 1   # 계단 0개 (아무 것도 안 하는 경우)
if n >= 1:
    dp[1] = 1

# 4. Bottom-up 방식으로 테이블 채우기
for i in range(2, n + 1):
    dp[i] = dp[i - 1] + dp[i - 2]

# 5. 결과 출력
print(f"{n}개의 계단을 오르는 방법의 수는 {dp[n]}가지입니다.")
